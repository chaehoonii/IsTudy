package com.hot6.project.vo;


public class JobVO {
	private int info_num;
	private String info_link;
	private String info_img;
	private String info_sal;
	private String info_com;
	private String info_job;
	
	public int getInfo_num() {
		return info_num;
	}
	public void setInfo_num(int info_num) {
		this.info_num = info_num;
	}
	public String getInfo_link() {
		return info_link;
	}
	public void setInfo_link(String info_link) {
		this.info_link = info_link;
	}
	public String getInfo_img() {
		return info_img;
	}
	public void setInfo_img(String info_img) {
		this.info_img = info_img;
	}
	public String getInfo_sal() {
		return info_sal;
	}
	public void setInfo_sal(String info_sal) {
		this.info_sal = info_sal;
	}
	public String getInfo_com() {
		return info_com;
	}
	public void setInfo_com(String info_com) {
		this.info_com = info_com;
	}
	public String getInfo_job() {
		return info_job;
	}
	public void setInfo_job(String info_job) {
		this.info_job = info_job;
	}
	
	
}
